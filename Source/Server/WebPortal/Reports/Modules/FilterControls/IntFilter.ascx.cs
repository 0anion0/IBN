		#region Implementation of IFilterControl
		public object Value
		{
			set
			{
				if (value!=null)
				{
					IntFilterValue ifValue = (IntFilterValue)value;
					if (ifValue.TypeValue=="3")
					{
						txtValue2.Text = ifValue.SecondValue;
						tdSecond.Style.Add("display","block");
					}
					else
						tdSecond.Style.Add("display","none");
					ddType.Value = ifValue.TypeValue;
					txtValue1.Text = ifValue.FirstValue;
				}
				else{
					ddType.Value = "0";
					tdSecond.Style.Add("display","none");
					txtValue1.Text = "";
					txtValue2.Text = "";
				}
			}
			get
			{
				object retval = null;
				if (int.Parse(ddType.Value)<3 && txtValue1.Text!="")
				{
					retval = new IntFilterValue(ddType.Value, txtValue1.Text, "");
				}
				else if (ddType.Value=="3" && txtValue1.Text!="" && txtValue2.Text!="")
				{
					retval = new IntFilterValue(ddType.Value, txtValue1.Text, txtValue2.Text);
				}
				return retval;
			}
		}
		
